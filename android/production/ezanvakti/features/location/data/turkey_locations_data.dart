  static List<String> getAllProvinces() {
    final provinces = _locations.map((loc) => loc.province).toSet().toList();
    provinces.sort();
    return provinces;
  }

  static List<Location> getDistrictsByProvince(String province) {
    return _locations.where((loc) => loc.province == province).toList()
      ..sort((a, b) => a.district.compareTo(b.district));
  }

  static Location? getLocationById(String id) {
    try {
      return _locations.firstWhere((loc) => loc.id == id);
    } catch (_) {
      return null;
    }
  }

  static List<Location> getAllLocations() {
    return List.unmodifiable(_locations);
  }

  static List<Location> searchLocations(String query) {
    if (query.isEmpty) return [];

    final lowerQuery = query.toLowerCase();
    return _locations.where((loc) {
      return loc.province.toLowerCase().contains(lowerQuery) ||
          loc.district.toLowerCase().contains(lowerQuery);
    }).toList();
  }
